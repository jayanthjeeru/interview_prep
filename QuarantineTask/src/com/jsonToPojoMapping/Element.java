package com.jsonToPojoMapping;

public class Element {
    private Object key;
    private String name;

    // Getters and Setters
    public Object getKey() {
        return key;
    }

    public void setKey(Object key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
